#include "ft_printf.h"

void	ft_putstrc_fd(char *s, int fd)
{
	if (!s)
	{
		ft_putstrc_fd("(null)", fd);
		return ;
	}
	while (*s != '\0')
	{
		ft_countc_fd(*s, fd, 0);
		++s;
	}
}
